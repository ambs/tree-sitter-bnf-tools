/// Skeleton `highlights.scm` generator.
///
/// Applies naming-convention heuristics to assign Tree-sitter capture names
/// to grammar rules. Rules whose bodies contain no terminals are omitted;
/// unrecognised rules get a `; TODO: @???` placeholder unless `no_todos` is set.
use std::fmt;
use std::fmt::{Display, Formatter};

use super::nodes::GrammarNode;
use super::types::Grammar;

/// Returns `true` if `node` contains at least one terminal (literal or pattern).
fn has_terminal(node: &GrammarNode) -> bool {
    match node {
        GrammarNode::TerminalLiteral(_) | GrammarNode::TerminalPattern(_) => true,
        GrammarNode::Sequence(v) | GrammarNode::Choice(v) => v.iter().any(has_terminal),
        GrammarNode::Optional(b)
        | GrammarNode::ZeroOrMore(b)
        | GrammarNode::OneOrMore(b)
        | GrammarNode::Token(b)
        | GrammarNode::TokenImmediate(b) => has_terminal(b),
        GrammarNode::Field(_, b) | GrammarNode::Prec(_, _, b) => has_terminal(b),
        GrammarNode::Alias(b, _) => has_terminal(b),
        GrammarNode::NonTerminal(_) => false,
    }
}

